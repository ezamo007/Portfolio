function create_profile(professor_info){
  var name = professor_info[0],
      position = professor_info[1],
      research = professor_info[2],
      email = professor_info[3],
      image = professor_info[4],
      link = professor_info[5];


  var html = `
            <div id = "profile", onmouseover = "profile_hover()">
                <div id = "profile_top">
                  <p class = "professor_name"> ` + name + `</p>
                  <p class = "professor_details"> ` + position + `</p>
                </div>

                <div id = "profile_middle">
                  <center>
                  <a href = ` + link + `>
                  <img src = ` + image + ` width = 100%>
                  </a>
                  </center>

                </div>

                <div id = "profile_bottom">
                  <p class = "professor_details"> ` + research + `</p>
                  </br>
                  <p class = "professor_details", color = blue>`+ email + `</p>
                </div>
            </div>
            `;
  return html;
}

